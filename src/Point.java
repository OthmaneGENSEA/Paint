public class Point {
    int x;
    int y;


    public Point (int x1,int y1){

        this.x = x1;

        this.y = y1;

    }

    public Point (){

        this.x = 0;

        this.y = 0;

    }

    public int getX (){

        return this.x ;

    }

    public int getY (){

        return this.y ;

    }
    public  void setX(int a){this.x=a;}
    public void setY(int b){this.y=b;}

}
